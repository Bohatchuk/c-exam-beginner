#include <unistd.h>

void	print_numbers(void)
{
	char n;

	n = '0';
	while (n >= '0' && n <= '9')
	{
		write (1, &n, 1);
		n++;
	}
}

int		main(void)
{
	print_numbers();
	return (0);
}